package 문자열_다루기;
/*
toUpperCase : 모두 대문자로 변환
toLowerCase : 모두 소문자로 변환
*/
public class ToUpperLowerCase {
    public static void main(String[] args) {

        String e = "Hello Java";

        System.out.println(e.toUpperCase());    // 대문자
        System.out.println(e.toLowerCase());    // 소문자

    }
}
